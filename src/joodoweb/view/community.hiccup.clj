[:h4 "Contributing"]
[:p "To contribute to the Joodo project, all you have to do is clone the master branch, build, and run all the tests. If you have <a href=\"https://github.com/technomancy/leiningen\" target=\"_blank\">Leiningen</a> and <a href=\"http://git-scm.com\" target=\"_blank\">Git</a>, you can do that by running the following commands from your terminal:"]

[:code [:pre "git clone https://github.com/slagyr/joodo.git
cd joodo
lein javac
lein spec"]]

[:p "Then you can make patches and submit them via pull requests on <a href=\"https://github.com/slagyr/joodo\" target=\"blank\">Joodo's GitHub Repo</a>"]

[:h4 "Chat"]
[:p "If you want to chat with us and other users go to <a href=\"http://webchat.freenode.net\">freenode's</a> browser based irc chat client and join the joodo channel."]